#!/bin/bash

# Скрипт для компиляции и запуска приложения "Генератор резюме"

echo "=== Компиляция Java приложения ==="

# Переходим в папку с исходным кодом
cd src

# Компилируем все Java файлы
javac *.java

# Проверяем успешность компиляции
if [ $? -eq 0 ]; then
    echo "✅ Компиляция завершена успешно!"
    echo ""
    echo "=== Запуск приложения ==="
    echo "Для входа используйте:"
    echo "Пользователь: newton"
    echo "Пароль: newton"
    echo ""
    
    # Запускаем приложение
    java Main
else
    echo "❌ Ошибка компиляции!"
    exit 1
fi